/**
 * One-off script: generate placeholder public assets for dev / pre-launch.
 * Run: node scripts/generate-placeholder-assets.mjs
 */
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const publicDir = join(__dirname, "..", "public");

const BRAND = { r: 30, g: 80, b: 50 };
const ACCENT = { r: 139, g: 30, b: 30 };
const FG = { r: 255, g: 252, b: 248 };

async function loadSharp() {
  try {
    return (await import("sharp")).default;
  } catch {
    console.error("Installing sharp for asset generation…");
    const { execSync } = await import("node:child_process");
    execSync("npm install --no-save sharp", { stdio: "inherit", cwd: join(__dirname, "..") });
    return (await import("sharp")).default;
  }
}

function svgIcon(label, size, subtitle = "") {
  const fontSize = Math.round(size * 0.38);
  const subSize = Math.round(size * 0.12);
  return Buffer.from(`<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 ${size} ${size}">
  <rect width="${size}" height="${size}" fill="rgb(${BRAND.r},${BRAND.g},${BRAND.b})"/>
  <rect x="${size * 0.08}" y="${size * 0.08}" width="${size * 0.84}" height="${size * 0.84}" rx="${size * 0.12}" fill="none" stroke="rgb(${ACCENT.r},${ACCENT.g},${ACCENT.b})" stroke-width="${Math.max(2, size * 0.04)}"/>
  <text x="50%" y="${subtitle ? "46%" : "54%"}" text-anchor="middle" dominant-baseline="middle" font-family="Georgia, serif" font-size="${fontSize}" font-weight="700" fill="rgb(${FG.r},${FG.g},${FG.b})">${label}</text>
  ${subtitle ? `<text x="50%" y="68%" text-anchor="middle" dominant-baseline="middle" font-family="Arial, sans-serif" font-size="${subSize}" fill="rgb(${FG.r},${FG.g},${FG.b})" opacity="0.85">${subtitle}</text>` : ""}
</svg>`);
}

function svgOg(title, subtitle) {
  return Buffer.from(`<svg xmlns="http://www.w3.org/2000/svg" width="1200" height="630" viewBox="0 0 1200 630">
  <defs>
    <linearGradient id="bg" x1="0%" y1="0%" x2="100%" y2="100%">
      <stop offset="0%" stop-color="rgb(${BRAND.r},${BRAND.g},${BRAND.b})"/>
      <stop offset="100%" stop-color="rgb(${ACCENT.r},${ACCENT.g},${ACCENT.b})"/>
    </linearGradient>
  </defs>
  <rect width="1200" height="630" fill="url(#bg)"/>
  <rect x="48" y="48" width="1104" height="534" rx="24" fill="none" stroke="rgba(255,252,248,0.25)" stroke-width="4"/>
  <text x="600" y="270" text-anchor="middle" font-family="Georgia, serif" font-size="72" font-weight="700" fill="rgb(${FG.r},${FG.g},${FG.b})">${title.replace(/&/g, "&amp;")}</text>
  <text x="600" y="360" text-anchor="middle" font-family="Arial, sans-serif" font-size="32" fill="rgb(${FG.r},${FG.g},${FG.b})" opacity="0.9">${subtitle.replace(/&/g, "&amp;")}</text>
  <text x="600" y="520" text-anchor="middle" font-family="Arial, sans-serif" font-size="22" fill="rgb(${FG.r},${FG.g},${FG.b})" opacity="0.65">Placeholder — replace before launch</text>
</svg>`);
}

async function png(sharp, svg, size, outPath) {
  await sharp(svg).resize(size, size).png({ compressionLevel: 9 }).toFile(outPath);
}

async function jpg(sharp, svg, outPath) {
  await sharp(svg).resize(1200, 630).jpeg({ quality: 82, mozjpeg: true }).toFile(outPath);
}

async function main() {
  const sharp = await loadSharp();
  await mkdir(publicDir, { recursive: true });

  const assets = [
    ["icon-16.png", () => png(sharp, svgIcon("VB", 16), 16, join(publicDir, "icon-16.png"))],
    ["icon-32.png", () => png(sharp, svgIcon("VB", 32), 32, join(publicDir, "icon-32.png"))],
    ["apple-icon.png", () => png(sharp, svgIcon("VB", 180, "Seva"), 180, join(publicDir, "apple-icon.png"))],
    ["icon-192.png", () => png(sharp, svgIcon("VB", 192, "Seva"), 192, join(publicDir, "icon-192.png"))],
    ["icon-512.png", () => png(sharp, svgIcon("VB", 512, "Seva"), 512, join(publicDir, "icon-512.png"))],
    ["logo.png", () => png(sharp, svgIcon("VB", 512, "Bhandara"), 512, join(publicDir, "logo.png"))],
    ["og-image.jpg", () => jpg(sharp, svgOg("Vrindavan Bhandara", "Sacred Seva in Vrindavan & Mathura"), join(publicDir, "og-image.jpg"))],
    ["og-vrindavan.jpg", () => jpg(sharp, svgOg("Seva in Vrindavan", "Book Bhandara, Brahmin Bhoj & Gau Seva"), join(publicDir, "og-vrindavan.jpg"))],
  ];

  for (const [name, write] of assets) {
    await write();
    console.log(`✓ public/${name}`);
  }

  await writeFile(
    join(publicDir, ".placeholder-assets"),
    "Generated by scripts/generate-placeholder-assets.mjs — replace with production artwork before launch.\n",
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
